export class HomePageComponent implements OnInit {
  // MatPaginator Output


  displayedColumns: string[] = ['position', 'name', 'weight', 'symbol'];
  dataSource: PeriodicElement[] = [];

  // MatPaginator Inputs
  length = ELEMENT_DATA.length;
  pageSize = 10;
  pageSizeOptions: number[] = [5, 10, 25, 100];

  constructor() {
    this.refreshData(0, this.pageSize);
  }

  ngOnInit(): void {
  }

  setPageSizeOptions(setPageSizeOptionsInput: string) {
    if (setPageSizeOptionsInput) {
      this.pageSizeOptions = setPageSizeOptionsInput.split(',').map(str => +str);
    }
  }

  private refreshData(pageIndex: number, pageSize: number) {
    let i = 0;
    let result: PeriodicElement[] = [];
    ELEMENT_DATA.forEach(aaa => {
      let from = (pageIndex) * pageSize;
      let to = (pageIndex + 1) * pageSize - 1;

      if (i >= from && i <= to) { result.push(aaa); }
      i++;
    });

    this.dataSource = result;
  }

  // used to build a slice of papers relevant at any given time
  public getPaginatorData(event: PageEvent): PageEvent {
    this.refreshData(event.pageIndex, event.pageSize);
    return event;
  }

}
